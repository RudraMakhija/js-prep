/*
Is javascript a statically typed or a dynamically typed language?
*/

/*

Statically typed language: type of variable is checked at the compile time.

Dynamically typed language: type of variable is checked at the run time.

Basically in javascript(a dynamically typed language), a variable can hold value of any data type.

For ex, C++ and Java are statically typed languages i.e. a variable can hold value of only the mentioned data type unlike javascript.

*/

var a= 100 // first 'a' holds the value of number data type
var a= "Hello"// then 'a' holds the value of string data type

console.log(a) //logs Hello